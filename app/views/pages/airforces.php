    <h2 id="polska">Polskie Siły Powietrzne</h2>
    <b>Siły Powietrzne, SP</b> – jeden z czterech rodzajów Sił Zbrojnych Rzeczypospolitej Polskiej, obok Wojsk Lądowych,
                               Marynarki Wojennej i Wojsk Specjalnych. Ich głównym zadaniem jest prowadzenie operacji
                               mających na celu uzyskanie przewagi w powietrzu i wspieranie oddziałów innych rodzajów
                               Sił Zbrojnych. Składają się z Wojsk Lotniczych, Wojsk Obrony Przeciwlotniczej i Wojsk
                               Radiotechnicznych.
    <br />
    <br />
                               Ich prekursorem były siły powietrzne Błękitnej Armii podczas I wojny światowej. Do 1
                               lipca 2004 roku nosiły nazwę Wojska Lotnicze i Obrony Powietrznej, WLOP, które z kolei
                               utworzono 1 lipca 1990 roku przez połączenie dwóch rodzajów sił zbrojnych: Wojsk
                               Lotniczych i Wojsk Obrony Powietrznej Kraju.
    <br />
