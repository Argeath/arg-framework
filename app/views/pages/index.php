        <h3>Lotnictwo</h3>
        <div>
            <b>Lotnictwo</b> – ogół zagadnień związanych z wszelkiego rodzaju statkami powietrznymi (samolotów,
                             śmigłowców, szybowców, balonów i in.). Lotnictwo dzieli się na cywilne (transportowe,
                             sportowe, gospodarczo-usługowe) i wojskowe.
            <br />
            <br />
                             W węższym znaczeniu lotnictwo to nazwa gałęzi gospodarki, w tym przemysłu (np. technologie
                             wytwarzania materiałów, systemów czy oprogramowania stosowanego), architektury obiektów
                             przemysłowych (np. lotniska i wielkie kompleksy portów lotniczych), transportu (ruch
                             lotniczy, w tym pasażerski oraz lotnictwo ogólne), handlu (np. dostawy dóbr i usługi
                             pocztowo-kurierskie) oraz usług społecznych (np. samoloty-kliniki okulistyczne) z użyciem
                             statków powietrznych (głównie samolotów).
            <br />
            <br />
                             W ujęciu wojskowym lotnictwo to element powietrznych sił zbrojnych, zwany wojskami
                             lotniczymi, którego podstawową bronią są różnego rodzaju statki powietrzne.
        </div>

        <h3>Historia</h3>
        <div>
            Wiele kultur usiłowało stworzyć i ujarzmić przedmioty latające. Najwcześniejszymi z nich były kamienie i
            włócznie, następnie bardziej skomplikowane jak bumerangi, latawce czy sztuczne ognie. Wczesne legendy
            opowiadały o ludziach wznoszących się w powietrze – w starożytnej Grecji był to mitologiczny Ikar, w Chinach
            mówiono o krótkich lotach z wykorzystaniem wielkich latawców.
            <br />
            <br />
            Rozpoczęcie współczesnego okresu lotnictwa datuje się na 21 listopada 1783 roku wraz z pierwszym
            udokumentowanym lotem balonu na gorące powietrze braci Montgolfier. Możliwości praktycznego wykorzystania
            pierwszych balonów do komunikacji były niewielkie, ponieważ ich lot był zależny od kierunku wiatru.
            <br />
            <br />
            Jeszcze w 1892 roku Lord Kelvin stwierdził, że skonstruowanie maszyn latających cięższych od powietrza jest
            niemożliwe.
        </div>

        <h3>Loty w kosmos</h3>
        <div>
            Lotnictwo na ogół nie obejmuje zagadnień związanych z lotami w kosmos, którymi zajmuje się astronautyka.
            Istnieją jednak lub zaproponowane zostały systemy, gdzie lot orbitalny lub suborbitalny, czy nawet
            opuszczenie strefy grawitacji Ziemi lotem międzyplanetarnym, jest do wykonania pojazdem kosmicznym
            startującym ze specjalnie przystosowanego samolotu wzniesionego na wysoki pułap lotu, gdzie energetyczny
            koszt rozpoczęcia lotu kosmicznego, i (co za tym idzie) potrzebna masa pojazdu z potrzebnym paliwem jest
            znacznie zmniejszona w porównaniu ze startem za pomocą wielkiej rakiety odpalonej z powierzchni.
        </div>


        <h3>Problemy lotnictwa</h3>
        <div>
            Dużym zagrożeniem dla wielu klas statków powietrznych są kolizje z ptakami. Wessanie zwierzęcia do turbiny
            silnika odrzutowego przy zderzeniu może spowodować oderwanie się łopatek turbiny, utratę prawidłowego
            wyważenia układu sprężarki dośrodkowej i turbiny, a nawet eksplozję całego silnika. Na ogół kolizja z
            ptakiem powoduje spadek mocy, który może prowadzić do przeciągnięcia, co za tym idzie gwałtownego
            przepadania skutkującego zderzeniem z ziemią. Natomiast samo uderzenie dużego ptaka w powierzchnie nośne czy
            kadłub samolotu potrafi poważnie uszkodzić lecący z dużą prędkością samolot, nawet do punktu katastrofalnego
            i uniemożliwienia dalszego lotu. Zderzenia z ptakami są niewielkim zagrożeniem dla samolotów lekkich, gdyż
            na ogół nie powodują znacznych uszkodzeń płatowca i układu napędowego.
            <br />
            <br />
            Do zminimalizowania tego zagrożenia, poprzez odstraszanie, stosuje się ptaki drapieżne.
        </div>

